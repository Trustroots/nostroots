/* eslint-env node */
const fs = require("fs");
const path = require("path");

const buildTime =
  process.env.NOSTROOTS_BROWSER_BUILD_TIME || new Date().toISOString();
const outputDir = path.join(__dirname, "..", "src", "generated");
const outputFile = path.join(outputDir, "build-time.ts");

fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(
  outputFile,
  `// Generated by scripts/write-build-time.js — do not edit.\nexport const BUILD_TIME = ${JSON.stringify(buildTime)};\n`,
);

console.log(`Wrote build time ${buildTime}`);
